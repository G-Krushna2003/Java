/*
 6. Write a program to print the reverse from 80 - 10.
Output: 80,79,78,77,..10

 */

package code.Assignment3;

public class code6 {
    public static void main(String[] args) {
        int num=10;

        for(int i=80;i>=num;i--){
            System.out.print(i+" ");
        }
        System.out.println();
    }
    
}
